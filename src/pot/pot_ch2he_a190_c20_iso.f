      subroutine spline_ch2he(vsp_jacek, r)
c  spline_ch2he.f
c  using Jacek's code to do spline_fit of pot
c  example:
c    K=57
c    call splinej(K,Rp,XX,b0,c0,d0)
c    VNO=sevalj(K, r, Rp, XX, b0,c0,d0)
c  where,
c  K  the number of points to be fitted
c  Rp, XX   orignal data, Rp & XX should have the same dimensions
c  r  new distance, where pot need to be calculated by spline-Fitchbur
c  VNO  pot at r, output
      implicit double precision (a-h,o-z)   
      integer k
      dimension b0(19,190),c0(19,190),a0(19,190),rr(19)
      dimension vsp_jacek(190)
      dimension vv(3610),v(19,190),vvec(19,190)
      data rr /3.5,4,4.5,5,5.5,6,6.5,7,7.5,8,8.5,9,9.5,10,11,
     : 12,13,15,20/
      data ifirst /0/
      if (ifirst.eq.0) then
         open (unit=10,file=
     :     'potdata/ch2he_a190.dat')

         read (10,*) vv
         close(10)

         do i=1,19
            do j=1,190         
              k=(i-1)*190+j
              v(i,j)=vv(k)
            enddo
         enddo          

         do j=1,190
            call dcopy(19,v(1,j),1,vvec(1,j),1)
* vvec contains potentials at all 19 points (rows) for each 190 theta/phi (columns)
* determine spline coefficients for all 190 theta/phi values
            call spline(19,rr,vvec(1,j),a0(1,j),b0(1,j),c0(1,j))
         enddo
         ifirst=1
      endif
      do j=1,190
* using previously determined spline coefficients to determine potential at all 190 theta/phi values
          vsp_jacek(j)=seval(19,r,rr,vvec(1,j),a0(1,j),b0(1,j),c0(1,j))
      enddo             
         
       end   



