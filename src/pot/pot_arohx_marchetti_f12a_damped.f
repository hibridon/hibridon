      call dgels('N',37,16,1,d0mat,37,vsum,37,work,lwork,info)
*      print 410, (vsum(j),j=1,16)
*410   format('vsum after dgels:  ', 16g14.6)
* detemine difference potential at angles
      ind=1
      do iang=1,35
         call dcopy(39,vl2(ind),1,vec,1)
         call dsplint(rr,vec,csplin2(1,iang),39,r,vdif(iang))
         ind=ind+39
      enddo
* solve linear equations for legendre expansion coefficients
* for difference potential
      call dcopy(350,d2,1,d2mat,1)
