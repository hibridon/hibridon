     : -7.2500000d-4, -5.6800000d-4, -4.4800000d-4, -2.8100000d-4,
     : -1.8500000d-4, -1.2400000d-4, -8.6000000d-5, -6.1000000d-5,
     : -4.5000000d-5, -3.5000000d-5, -2.2000000d-5, -1.7000000d-5,
     : -2.1000000d-5/
      data ifirst /0/
      if (ifirst .eq. 0) then
* spline fit of the vl0 and vl2 coefficients
         ind=1
         do iang=1,37
            call dcopy(39,vl0(ind),1,vec,1)
*    evaluate derivative at first point
            der1=(vec(2)-vec(1))/(rr(2)-rr(1))
            call dspline(rr,vec,39,der1,0d0,csplin0(1,iang))
            ind=ind+39
         enddo
* fit last three points to r^-6
         c6=0d0
         do ir=37,39
            vav=dsum(37,vl0(ir),39)/37d0
*  fit to r^-6
            c6=c6+vav*rr(ir)**6
         enddo
         c6=c6/3d0
         ind=1
         do iang=1,35
            call dcopy(39,vl2(ind),1,vec,1)
*    evaluate derivative at first point
            der1=(vec(2)-vec(1))/(rr(2)-rr(1))
            call dspline(rr,vec,39,der1,0d0,csplin2(1,iang))
            ind=ind+39
         enddo
         ifirst=1
      endif
* detemine sum potential at angles
      ind=1
      do iang=1,37
         call dcopy(39,vl0(ind),1,vec,1)
         call dsplint(rr,vec,csplin0(1,iang),39,r,vsum(iang))
         ind=ind+39
      enddo
