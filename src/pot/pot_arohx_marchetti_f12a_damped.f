*415   format('vdif before gdels= ', 37g14.6)
      call dgels('N',35,10,1,d2mat,35,vdif,35,work,lwork,info)
*      print 420, (vdif(j),j=1,10)
*420   format('vdif after dgels:  ', 10g14.6)
* determine switching function
      switch=one-0.5d0*(tanh(1d0*(r-22.5d0))+one)
*      print *, 'switch, vsum before switch:  ', switch,vsum(1)
      vsum(1)=vsum(1)*switch+(one-switch)*c6*r**(-6)
*      print *, 'switch, vsum after switch:  ', switch,vsum(1)
      call dscal(16,switch,vsum(2),1)
      call dscal(10,switch,vdif,1)
* convert to hartree
      conv=1.d0/219474.6d0
      call dscal(16,conv,vsum,1)
      call dscal(10,conv,vdif,1)
* store potential expansion terms
      vv0=vsum(1)
      call dcopy(15,vsum(2),1,vvl,1)
      call dcopy(10,vdif(1),1,vvl(16),1)
      return
