* ----------------------------------------------------------------------
      subroutine pot (vv0, r)
*  subroutine to calculate the r-dependent coefficients
*  in atomic units (distance and energy)
* ----------------------------------------------------------------------
*  on entry:
*    r:      interparticle distance
*  on return:
*  vv0        contains isotropic term (d00 term in vsum)
*  variable in common block /covvl/
*    vvl:     vector of length 6 to store r-dependence of each term
*             in potential expansion
*    vvl(1-12) expansion coefficients in dl0 (l=1:12) of vsum
*    vvl(13-23) expansion coefficients in dl2 (l=2:12) of vdif

* uses linear least squares routines from cmlib
* ----------------------------------------------------------------------

      implicit double precision (a-h,o-z)

      common /covvl/ vvl(23)
      data half, zero, one, alph /0.5d0, 0.d0, 1.d0, 1.2d0/
      call dscal(23,zero,vvl,1)
* convert to hartree
      conv=1.d0/219474.6
      vv0=VL0_0(r)*conv
      vvl(1)=VL0_1(r)*conv
      vvl(2)=VL0_2(r)*conv
      vvl(3)=VL0_3(r)*conv
      vvl(4)=VL0_4(r)*conv
      vvl(5)=VL0_5(r)*conv
      vvl(6)=VL0_6(r)*conv
      vvl(7)=VL0_7(r)*conv
      vvl(8)=VL0_8(r)*conv
      vvl(9)=VL0_9(r)*conv
      vvl(10)=VL0_10(r)*conv
      vvl(11)=VL0_11(r)*conv
      vvl(12)=VL0_12(r)*conv
      vvl(13)=VL2_2(r)*conv
      vvl(14)=VL2_3(r)*conv
      vvl(15)=VL2_4(r)*conv
      vvl(16)=VL2_5(r)*conv
      vvl(17)=VL2_6(r)*conv
      vvl(18)=VL2_7(r)*conv
      vvl(19)=VL2_8(r)*conv
      vvl(20)=VL2_9(r)*conv
      vvl(21)=VL2_10(r)*conv
      vvl(22)=VL2_11(r)*conv
      vvl(23)=VL2_12(r)*conv
      end

