                endif
                if (jja(i).eq.2) then
                   if (j12(i).eq.2) then
                      const_pi(i)=cpi(1)
                      const_2sig(i)=c2sig(1)
                      const_4sig(i)=c4sig(1)
                   elseif (j12(i).eq.1) then
                      const_pi(i)=cpi(2)
                      const_2sig(i)=c2sig(2)
                      const_4sig(i)=c4sig(2)
