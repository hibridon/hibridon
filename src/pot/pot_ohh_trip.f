* check if desired values or r and R are outside of range
* if R .lt. 3 bohr, the potential are set equal to their values at R=3
* if 1.4<r or r>2.7, then the interaction potential is taken equal to its
* value at these limits
* NOTE that  the calculated OH potential at the chosen
* value of r is added at the end


      if (rr.lt.3) then
         rbig=3d0
      else
         rbig=rr
      endif
      if (r.lt.1.4d0) then
         rsm=1.4d0
      elseif (r.gt.2.7d0) then
         rsm=2.7d0
      else
         rsm=r
      endif

* determine potentials for each state at each of grid points in roh and gamma
      xrr=1-exp(-(rbig-rro)/alphrr)
      switch=half*(one+tanh(rbig-8.5d0))
      ir=3
        call dcopy(288,cr9(1,1),1,cr(1,1),1)
        call dcopy(36,alphlr9(1,1),1,alphlr(1,1),1)
        call dcopy(36,alr9(1,1),1,alr(1,1),1)
        do igam=1,9
            nlow=(igam-1)*8
            do istate=1,4
               vpoly=polyval(cr(nlow+1,istate),xrr,7)
               vexp=alr(igam,istate)*exp(alphlr(igam,istate)*rbig)
               v(ir,igam,istate)=(one-switch)*vpoly+switch*vexp
            enddo
         enddo
* now setup matrix for least squares fit in the angle gamma
      if (ifirst.eq.0) then
* first for fit of vsum in legendre polynomials
         call dcopy(81,d0,1,ag0,1)
         call dqrank(ag0,9,9,9,tol,kg0,kpvtg0,qrauxg0,work)
* then for fit of vdif in d20 reduced matrix elements
         call dcopy(49,d2,1,ag2,1)
         call dqrank(ag2,7,7,7,tol,kg2,kpvtg2,qrauxg2,work)
      endif
* now determine each of the potentials at the input value of roh and all value
      do igam=1,9
         do istate=1,4
            vgamma(istate)=v(3,igam,istate)
         enddo
*  determine vsum and vdif for both the a' and a" pes's
         do imult=1,2
            ind=(imult-1)*2
            vsum(igam,imult)=0.5*(vgamma(ind+1)+vgamma(ind+2))
            vdif(igam,imult)=0.5*(-vgamma(ind+1)+vgamma(ind+2))
         enddo
      enddo
      do imult=2,2
