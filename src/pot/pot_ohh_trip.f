         call dcopy(9,vsum(1,imult),1,vgam,1)
         call dqrlss(ag0,9,9,9,kg0,vgam,xgam,rsd,kpvtg0,qrauxg0)
         call dcopy(9,xgam,1,xgam0(1,imult),1)
* xgam0 contains the coefficients
* now fit vdif to expansion in reduced rotation matrix elements
         call dcopy(7,vdif(2,imult),1,vgam,1)
         call dqrlss(ag2,7,7,7,kg2,vgam,xgam,rsd,kpvtg2,qrauxg2)
         call dcopy(7,xgam,1,xgam2(1,imult),1)
      enddo
* xgam2 contains the coefficients
* transform expansion coefficients into hartree
      call dscal(9,tohar,xgam0(1,2),1)
      call dscal(7,tohar,xgam2(1,2),1)
* copy expansion coefficients into vv0 and vvl
      vv0=xgam0(1,2)
      call dcopy(8,xgam0(2,2),1,vvl(1),1)
      call dcopy(7,xgam2(1,2),1,vvl(9),1)
      ifirst=1
      return
      end


      function polyval(c,x,n)
* to evaluate a polynomial of degree n in the variable x
* this uses the reverse ordering of matlab
* sum(i,0:n) c(n-i)*x^i
      implicit double precision (a-h,o-z)
      dimension c(1)
      polyval=c(n+1)
*     print *,polyval
      if (n.eq.1) return
      xx=x
      do i=2,n+1
         polyval=polyval+c(n-i+2)*xx
*        print *,n,i,xx,c(n-i+2),polyval
         xx=xx*x
      enddo
      return
      end
